import { expectType } from 'tsd';
import CalHeatmap from '../src/CalHeatmap';
const cal = new CalHeatmap();
expectType(cal);
expectType(cal.paint());
expectType(cal.paint({}));
expectType(cal.paint({ range: 3 }));
expectType(cal.paint({}, []));
expectType(cal.next());
