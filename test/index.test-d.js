expectType(cal.jumpTo(new Date()));
expectType(cal.fill());
expectType(cal.fill(''));
expectType(cal.fill([]));
expectType(cal.destroy());
expectType(cal.dimensions());
expectType(cal.on('eventName', () => { }));
expectType(cal.addTemplates({}));
expectType(cal.extendDayjs(() => () => { }));
